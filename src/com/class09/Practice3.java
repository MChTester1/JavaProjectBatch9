public class Practice3 {

	public static void main(String[] args) {
		/* Reverse Numbers SQUARE
		 * 
		 */
		for(int r=5; r>=1; r--) {
			for(int c=6; c>=1;c--) {
				System.out.print(c);
	 }
				System.out.println();
 }
		for(int r=1; r<=5; r++) {
			for(int c=6; c>=1;c--) {
				System.out.print(c);
	 }
				System.out.println();
	}

}
}
